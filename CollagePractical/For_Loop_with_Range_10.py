# Program to demonstrate use of range() in a for loop

print("Counting from 1 to 10:")
for i in range(1, 11):
    print(i, end=' ')

print("\n\nEven numbers from 2 to 20:")
for i in range(2, 21, 2):
    print(i, end=' ')

print("\n\nCounting backwards from 10 to 1:")
for i in range(10, 0, -1):
    print(i, end=' ')
